# Operasi yang dapat dilakukan dengan penyingkatan
# Operasi ditambah dengan assingment

a = 5 # adalah assignment
print("nilai a =", a)

a += 1 # artinya adalah a = a + 1
print("nilai a += 1 adalah", a)

a *= 5 # artinya adalah a = a * 5
print("nilai a *= 5 adalah", a)

a /= 5 # artinya adalah a = a / 2
print("nilai a /= 2 adalah", a)

b = 10
print("\nnilai b =",b)

# Modulus dan Floor Division
b %= 3
print("nilai b %= 3 adalah", b)

b = 10
print("\nnilai b =",b)

b //= 3
print("nilai b //= 3 adalah", b)

# Pangkat atau Eksponen
a = 5
print("nilai a =", a)
a **= 3
print("nilai a **- 3, nilai a adalah", a)

# Operasi bitwise
# OR
c = True
print("\nnilai c =", c)
c |= False
print("nilai c |= False, nilai c adalah", c)
c = False
print("\nnilai c =", c)
c |= False
print("nilai c |= False, nilai c adalah", c)

# AND
c = True
print("\nnilai c =", c)
c &= False
print("nilai c &= False, nilai c adalah", c)
c = True
print("\nnilai c =", c)
c &= True
print("nilai c &= True, nilai c adalah", c)

# XOR
c = True
print("\nnilai c =", c)
c ^= False
print("nilai c ^= False, nilai c adalah", c)
c = True
print("\nnilai c =", c)
c ^= True
print("nilai c ^= True, nilai c adalah", c)