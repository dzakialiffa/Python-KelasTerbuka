# Program if inline
nama = input("Siapa nama Anda?")
if nama =="Dzaki" : print("Kamu ganteng abiezz")
print(f"Terima kasih {nama}")

# Program if identation
if nama =="Dzaki":
    print("kamu ganteng abiezz")
    print("Kamu juga keren abiezz")
print(f"Terima kasih {nama}")

# Else statement
if nama == "Aliffa":
    print("Hai Aliffa, si keren")
else:
    print("Kok kamu bukan Aliffa sih")
