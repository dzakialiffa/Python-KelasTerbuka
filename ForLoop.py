# Ini dengan list
angka_list = [0,1,2,3,4,5] # ini adalah list
print(angka_list)

for i in angka_list:
    print(f"i sekarang -> {i}")
print("akhir program angka list")

# ini dengan range
angka_range = range(5)

for i in angka_range:
    print(f"i sekarang -> {i}")
print("akhir program angka range")

angka_range2 = range (1,10)

for i in angka_range2:
    print(f"i sekarang -> {i}")
print("Akhir dari program range batas 1 dan 10")

