# List adalah Array di Piton, mengakses data pake INDEX

data_list = ["Aku", "Anak", "Baik"]
print(data_list[2])

# Dictionary (dict) -> associative array
# Identifier -> Key

data_dictionary = {
    'key':'value',
    'dz':'Dzaki',
    'gt':'Ganteng',
    'nmr': 1200,
    'list':data_list
}
print(data_dictionary['dz']) # Untuk panggil data
print(data_dictionary['nmr']) # Untuk panggil data nomor
print(data_dictionary['list']) # Untuk panggil data list